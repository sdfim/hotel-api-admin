    protected $signature = 'move-db:informative-services';

    protected $description = 'Import Informative Services from donor database';

    public function handle()
    {
        $this->warn('-> M step Import Informative Services');

        $donorServices = DB::connection('donor')->table('default_hotel_informative_services')
            ->leftJoin('currencies', 'default_hotel_informative_services.currency_id', '=', 'currencies.id')
            ->select(
                'default_hotel_informative_services.*',
                'currencies.code as currency_code'
            )
            ->get();

        $this->newLine();

        $this->withProgressBar($donorServices, function ($donorService) {
            $hotel = Hotel::whereHas('crmMapping', function ($query) use ($donorService) {
                $query->where('crm_hotel_id', $donorService->hotel_id);
            })->first();

            if (! $hotel || ! $hotel->product) {
                return;
            }

            $serviceName = $this->serviceMapper[preg_replace('/\s+/', ' ', $donorService->name)]
                ?? $this->serviceMapper[$donorService->name]
                ?? 'Unknown';

            $configServiceType = ConfigServiceType::firstOrCreate(
                ['name' => $serviceName],
                ['description' => $serviceName, 'cost' => 0]
            );

            $productInformativeService = ProductInformativeService::updateOrCreate(
                [
                    'product_id' => $hotel->product->id,
                    'name' => $donorService->name,
                ],
                [
                    'service_id' => $configServiceType ? $configServiceType->id : null,
                    'start_date' => now(),
                    'end_date' => now()->addYear(),
                    'cost' => $donorService->total_rack,
                    'total_net' => $donorService->total_rack,
                    'apply_type' => $this->serviceApplyTypeMapper[$serviceName] ?? ProductServiceApplyTypeEnum::PER_NIGHT,
                    'currency' => $donorService->currency_code,
                    'service_time' => $donorService?->time ? (new \DateTime($donorService->time))->format('h:i A') : null,
                    'show_service_on_pdf' => $donorService->show_on_pdf,
                    'show_service_data_on_pdf' => $donorService->show_date_on_pdf,
                    'commissionable' => false,
                    'auto_book' => $donorService->auto_book,
                ]
            );

            $columns = json_decode($donorService->columns, true);

            // Handle dynamic columns
            if (! empty($columns)) {
                foreach ($columns as $column) {
                    $value = Arr::get($column, 'value', null);
                    $name = Arr::get($column, 'name', null);
                    if (! $value || ! $name) {
                        continue;
                    }
                    ProductInformativeServiceDynamicColumn::updateOrCreate(
                        [
                            'product_informative_service_id' => $productInformativeService->id,
                            'name' => Arr::get($column, 'name'),
                        ],
                        [
                            'value' => Arr::get($column, 'value'),
                        ]
                    );
                }
            }

            $this->output->write("\033[1A\r\033[KAdded informative service for hotel ID {$hotel->id} with name {$donorService->name}.\n");
        });

        $this->info("\nInformative services imported successfully.");
    }

